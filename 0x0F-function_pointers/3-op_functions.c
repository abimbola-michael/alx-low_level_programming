#include "3-cal.h"

/**
 * op_add - add numbers
 * @a: input int
 * @b: input int
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract numbers
 * @a: input int
 * @b: input int
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply numbers
 * @a: input int
 * @b: input int
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide numbers
 * @a: input int
 * @b: input int
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - remainder numbers
 * @a: input int
 * @b: input int
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}

